    private Personne bindPersonne() throws NumberFormatException {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd", Locale.getDefault());
        String d = sdf.format(date.getDate());
        
        Personne p=new Personne();
        
        p.setCIN(CIN.getText());
        p.setNom(Nom.getText());
        p.setPrenom(Prenom.getText());
        p.setSexe(cmd.getSelectedItem().toString());
        p.setAdresse(adresse.getText());
        p.setDateNaissance(d);
        
        return p;
    }
    

