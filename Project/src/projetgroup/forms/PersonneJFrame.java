    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
      if(Prenom.getText().isEmpty() || CIN.getText().isEmpty() || Nom.getText().isEmpty() || adresse.getText().isEmpty()){
          JOptionPane.showMessageDialog(this,"Il faut remplis les champs");
      }else{
          if(personneDAO.getPersonneByCIN((CIN.getText()))==false){
              personneDAO.addPersonne(bindPersonne());
              Remplissage();
              Vider();
              JOptionPane.showMessageDialog(this,"Ajouter");
          } else {
              JOptionPane.showMessageDialog(this,"Cette Personne existe dÃ©jÃ¢");
              
          }
      }   
    }//GEN-LAST:event_jButton1MouseClicked

