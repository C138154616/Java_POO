    public void Remplissage(){
       String columns[]={"ID","CIN","Nom","Prenom","Sexe","Date Naissance","Adresse"};
       DefaultTableModel model=new DefaultTableModel(columns,0);
       Object[] row;
       for(Personne P:personneDAO.getAllPersonne()){
           row=new Object[7];
           row[0]=P.getIdPersonne();
           row[1]=P.getCIN();
           row[2]=P.getNom();
           row[3]=P.getPrenom();
           row[4]=P.getSexe();
           row[5]=P.getDateNaissance();
           row[6]=P.getAdresse();
           model.addRow(row);
           
       }
      jTable1.setModel(model);
    }
