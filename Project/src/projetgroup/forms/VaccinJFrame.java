    public void Remplissage(){
       String columns[]={"ID","Type Vaccin","Description"};
       DefaultTableModel model=new DefaultTableModel(columns,0);
       Object[] row;
       for(Vaccin V:vaccinDao.getAllVaccin()){
           row=new Object[3];
           row[0]=V.getVaccinId();
           row[1]=V.getType();
           row[2]=V.getDescription();
           model.addRow(row);
           
       }
      jTable1.setModel(model);
    }
    private Vaccin bindVaccin() throws NumberFormatException {
        
        
        Vaccin V=new Vaccin();
        
        V.setType(txttype.getText());
        V.setDescription(txtDes.getText());
        return V;
    }
