    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if(txttype.getText().isEmpty() || txtDes.getText().isEmpty()){
          JOptionPane.showMessageDialog(this,"Il faut remplis les champs");
        }else{
        vaccinDao.addVaccin(bindVaccin());
        Remplissage();
        Vider();
        JOptionPane.showMessageDialog(this,"Ajouter");
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int i=jTable1.getSelectedRow();
        Vaccin V=vaccinDao.getVaccinById(Integer.parseInt(jTable1.getValueAt(i, 0).toString()));
        txttype.setText(V.getType());
        txtDes.setText(V.getDescription());
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
       if(txttype.getText().isEmpty() || txtDes.getText().isEmpty()){
          JOptionPane.showMessageDialog(this,"Il faut remplis les champs");
        }else{
        int i=jTable1.getSelectedRow();
        Vaccin V=bindVaccin();
        V.setVaccinId(Integer.parseInt(jTable1.getValueAt(i, 0).toString()));
        vaccinDao.updateVaccin(V);
        Remplissage();
        Vider();
        JOptionPane.showMessageDialog(this,"Modifier");
       }
    }//GEN-LAST:event_jButton2MouseClicked

    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
       int i=jTable1.getSelectedRow();
        Vaccin V=vaccinDao.getVaccinById(Integer.parseInt(jTable1.getValueAt(i, 0).toString()));
        vaccinDao.deleteVaccin(V.getVaccinId());
        Remplissage();
        Vider();
    }//GEN-LAST:event_jButton3MouseClicked

